    // Tests INV-001 [unit] (codex F9): exact singleton-range constraints on
    // every PackageVersion — plain Version="4.11.0" is a minimum-inclusive range.
    [Fact]
    public void AllPackageVersions_AreExactSingletonRanges()
    {
        var doc = SpikePaths.Xml(SpikePaths.P("Directory.Packages.props"));
        var versions = doc.Descendants("PackageVersion").ToList();
        Assert.NotEmpty(versions);
        foreach (var pv in versions)
        {
            var v = pv.Attribute("Version")!.Value;
            Assert.Matches(@"^\[[^,\[\]]+\]$", v);
        }
        // The three spec-named singleton pins, verbatim.
        string PinOf(string id) => versions.Single(p => p.Attribute("Include")!.Value == id).Attribute("Version")!.Value;
        Assert.Equal("[4.11.0]", PinOf("DafnyCore"));
        Assert.Equal("[4.11.0]", PinOf("DafnyPipeline"));
        Assert.Equal("[4.11.0]", PinOf("DafnyDriver"));
        Assert.Equal("[3.5.5]", PinOf("Boogie.ExecutionEngine"));
        Assert.Equal("[2.0.0-beta4.22272.1]", PinOf("System.CommandLine"));
    }

