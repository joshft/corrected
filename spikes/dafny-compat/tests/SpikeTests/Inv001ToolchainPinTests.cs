    // Tests INV-001 [integration] (RS-014a, codex F9, AP-011, TA-B3a):
    // behavioral stale-lock negative test with TEST-owned fault construction.
    // The TEST copies the Route A project graph + sealing/config files into
    // scratch, runs ONE GENERIC restore phase (control leg) which must SUCCEED,
    // then the TEST ITSELF edits the lock JSON (DafnyCore -> nightly), verifies
    // the mutation landed on disk, and re-runs the IDENTICAL generic phase —
    // which must fail with the locked-mode mismatch diagnostic (NU1004-class).
    // Any-failure (network, missing package) cannot satisfy this: the control
    // leg already proved the same phase succeeds on the pristine copy.
    [Fact]
    public void StaleLockMutation_TestEditedLock_FailsWithNU1004_AfterSuccessfulControlRestore()
    {
        var scratch = SpikePaths.TestScratch("inv001-stale-lock");

        // TEST-owned scratch copy: sealing/config roots + contracts + Route A seam.
        foreach (var rootFile in new[] { "NuGet.Config", "global.json", "Directory.Packages.props", "Directory.Build.props", "Directory.Build.targets", "Directory.Build.rsp" })
        {
            File.Copy(SpikePaths.P(rootFile), Path.Combine(scratch, rootFile), overwrite: true);
        }
        SpikePaths.CopyTree(SpikePaths.P("contracts"), Path.Combine(scratch, "contracts"));
        SpikePaths.CopyTree(SpikePaths.P("adapters", "SpikeDafnyAdapter.RouteA"), Path.Combine(scratch, "adapters", "SpikeDafnyAdapter.RouteA"));
        var scratchProj = Path.Combine(scratch, "adapters", "SpikeDafnyAdapter.RouteA", "SpikeDafnyAdapter.RouteA.csproj");
        var scratchLock = Path.Combine(scratch, "adapters", "SpikeDafnyAdapter.RouteA", "packages.lock.json");

        // Phase 1 — CONTROL: the generic restore phase on the pristine copy must SUCCEED.
        var control = Launch.Script("scripts/run-spike.sh", null, "--phase", "restore", "--project", scratchProj);
        Assert.True(control.ExitCode == 0,
            $"control restore failed (exit {control.ExitCode}); an already-broken restore cannot prove locked mode. stderr: {control.StdErr}");

        // Phase 2 — the TEST mutates ONLY the lock-consistency data. TEST_BUG
        // fix #5 (endorsed): mutate the REQUESTED range for DafnyCore (a
        // lock-vs-project-declaration inconsistency, the canonical
        // deterministic offline NU1004 trigger, proving lock VALIDATION was in
        // effect — RS-014a), bumping resolved alongside so the lock stays
        // self-consistent while disagreeing with the project's pin.
        const string nightly = "4.11.1-nightly-2026-01-01";
        var mutated = MutateDafnyCoreRequestedRange(File.ReadAllText(scratchLock), nightly);
        File.WriteAllText(scratchLock, mutated);
        Assert.Contains(nightly, File.ReadAllText(scratchLock)); // the fault really landed on disk (TA-B3a)

        // Phase 3 — the IDENTICAL generic phase must now fail with the
        // locked-mode mismatch diagnostic. NU1004 ONLY: NU1102-class
        // missing-package failures are the spec-excluded any-failure shape.
        var negative = Launch.Script("scripts/run-spike.sh", null, "--phase", "restore", "--project", scratchProj);
        Assert.NotEqual(0, negative.ExitCode);
        Assert.Contains("NU1004", negative.StdOut + negative.StdErr);
    }

    private static string MutateDafnyCoreRequestedRange(string lockJson, string nightlyVersion)
    {
        var nightlyRange = $"[{nightlyVersion}, {nightlyVersion}]";
        using var doc = System.Text.Json.JsonDocument.Parse(lockJson);
        using var stream = new MemoryStream();
        using (var writer = new System.Text.Json.Utf8JsonWriter(stream, new System.Text.Json.JsonWriterOptions { Indented = true }))
        {
            void WriteElement(System.Text.Json.JsonElement el, bool insideDafnyCore)
            {
                switch (el.ValueKind)
                {
                    case System.Text.Json.JsonValueKind.Object:
                        writer.WriteStartObject();
                        foreach (var prop in el.EnumerateObject())
                        {
                            writer.WritePropertyName(prop.Name);
                            if (insideDafnyCore && prop.Name == "requested")
                            {
                                writer.WriteStringValue(nightlyRange);
                            }
                            else if (insideDafnyCore && prop.Name == "resolved")
                            {
                                writer.WriteStringValue(nightlyVersion);
                            }
                            else
                            {
                                WriteElement(prop.Value, insideDafnyCore || prop.Name == "DafnyCore");
                            }
                        }
                        writer.WriteEndObject();
                        break;
                    case System.Text.Json.JsonValueKind.Array:
                        writer.WriteStartArray();
                        foreach (var item in el.EnumerateArray()) { WriteElement(item, insideDafnyCore); }
                        writer.WriteEndArray();
                        break;
                    default:
                        el.WriteTo(writer);
                        break;
                }
            }
            WriteElement(doc.RootElement, insideDafnyCore: false);
        }
        return System.Text.Encoding.UTF8.GetString(stream.ToArray());
    }

    // Tests INV-001 [integration] (TA-B3b): the effective build inherits no
    // props from above the spike root — proven against the RESOLVED IMPORT
    // LIST, parsed and judged BY THE TEST, not by a script-emitted OK marker.
    [Fact]
    public void EffectiveBuild_InheritsNoPropsAboveSpikeRoot()
    {
        var result = Launch.Script("scripts/run-spike.sh", null, "--phase", "preprocess-audit");
        Assert.True(result.ExitCode == 0, $"preprocess audit failed: {result.StdErr}");

        var imports = result.StdOut.Split('\n')
            .Where(l => l.StartsWith("IMPORT ", StringComparison.Ordinal))
            .Select(l => l["IMPORT ".Length..].Trim())
            .ToList();
        Assert.True(imports.Count > 0, "preprocess audit emitted no IMPORT lines — nothing was audited (AP-010)");

        var repoRoot = Path.GetFullPath(SpikePaths.RepoRoot) + Path.DirectorySeparatorChar;
        var spikeRoot = Path.GetFullPath(SpikePaths.SpikeRoot) + Path.DirectorySeparatorChar;
        foreach (var import in imports)
        {
            var full = Path.GetFullPath(import);
            var insideRepo = full.StartsWith(repoRoot, StringComparison.Ordinal);
            var insideSpike = full.StartsWith(spikeRoot, StringComparison.Ordinal);
            Assert.False(insideRepo && !insideSpike,
                $"MSBuild import leaks from above the spike root: {import} (INV-001 sealing violated)");
        }
    }
