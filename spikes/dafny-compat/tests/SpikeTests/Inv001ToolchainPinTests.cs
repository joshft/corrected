    // Tests INV-001 [unit]: every Dafny*/Boogie.* package in every project's
    // resolved graph is exact-pinned, with a committed packages.lock.json per project.
    // Source: spikes/dafny-compat/adapters/SpikeDafnyAdapter.RouteA/packages.lock.json
    // (real producer output: generated by `dotnet restore` at the pinned SDK, committed in-repo — AP-014).
    [Fact]
    public void LockFiles_ExistPerProject_AndPinDafnyBoogieFamilyExactly()
    {
        foreach (var (name, _) in SpikePaths.Projects)
        {
            var lockPath = SpikePaths.LockFilePath(name);
            Assert.True(File.Exists(lockPath), $"missing committed packages.lock.json for {name} (INV-001)");

            using var doc = SpikePaths.Json(lockPath);
            foreach (var fw in doc.RootElement.GetProperty("dependencies").EnumerateObject())
            {
                foreach (var dep in fw.Value.EnumerateObject())
                {
                    var id = dep.Name;
                    var isDafny = id.StartsWith("Dafny", StringComparison.Ordinal);
                    var isBoogie = id.StartsWith("Boogie.", StringComparison.Ordinal);
                    if (!isDafny && !isBoogie)
                    {
                        continue;
                    }

                    var resolved = dep.Value.GetProperty("resolved").GetString();
                    var expectedFamilyVersion = isDafny ? "4.11.0" : "3.5.5";
                    Assert.True(resolved == expectedFamilyVersion,
                        $"{name}/{fw.Name}: {id} resolved {resolved}, expected {expectedFamilyVersion} (INV-001: no member of the family may float off-pin)");
                }
            }
        }

        // The four spec-named pins must be present in the seam graphs they anchor.
        AssertLockContains("SpikeDafnyAdapter.RouteA", "DafnyDriver", "4.11.0");
        AssertLockContains("SpikeDafnyAdapter.RouteA", "DafnyCore", "4.11.0");
        AssertLockContains("SpikeDafnyAdapter.RouteB", "DafnyCore", "4.11.0");
        AssertLockContains("SpikeDafnyAdapter.RouteB", "DafnyPipeline", "4.11.0");
        AssertLockContains("SpikeDafnyAdapter.RouteB", "Boogie.ExecutionEngine", "3.5.5");
    }

    private static void AssertLockContains(string project, string package, string version)
    {
        using var doc = SpikePaths.Json(SpikePaths.LockFilePath(project));
        var found = false;
        foreach (var fw in doc.RootElement.GetProperty("dependencies").EnumerateObject())
        {
            if (fw.Value.TryGetProperty(package, out var dep))
            {
                found = true;
                Assert.Equal(version, dep.GetProperty("resolved").GetString());
            }
        }
        Assert.True(found, $"{project} lock must contain {package} (DD-001 route graph)");
    }

